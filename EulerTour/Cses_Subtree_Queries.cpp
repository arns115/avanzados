struct Node{
	ll val = 0;
};
Node operator+(Node a,Node b){return {a.val+b.val};}

class Segment_Tree{
	public:
	int n,k=0;
	vector<Node> tree;

	Segment_Tree(int x = 1e5+10){
		n = x;
		while((1<<k)<n)k++;
		tree.resize(2*(1<<k)+1);
	}

	void build(vi &arr){
		for(int i=0;i<n;i++)tree[(1<<k)+i] = {arr[i]};
		for(int i = (1<<k)-1; i>0;i--)tree[i] = tree[i*2]+tree[i*2+1];
	}

	void update(int a, ll b){
		a+= (1<<k);
		tree[a] = {b};
		for(a/=2;a>=1;a/=2)tree[a] = tree[a*2]+tree[a*2+1];
	}

	ll query(int a,int b){
		a += (1<<k);
		b += (1<<k);
		Node s;
		while(a<=b){
			if(a%2 == 1)s = s+ tree[a++];
			if(b%2 == 0)s = s+ tree[b--];
			a/=2;
			b/=2;
		}
		return s.val;
	}
};



int timer = 0;
vi start,final;
vector<vi> adj;
void dfs(int u = 0,int prev = -1){
	start[u]=timer++;
	for(int v:adj[u]){
		if(v!=prev)dfs(v,u);
	}
	final[u] = timer;
}


